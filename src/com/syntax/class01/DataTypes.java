package com.syntax.class01;

public class DataTypes {
	
	public static void main(String[] args) {
		
		//representing and storing whole numbers
		byte box1=127; //range from -128 till 127
		short box2=32767; //-32768 to 32767
		int box3=1000; //most popular data type to use for whole numbers
		long box4=9900000000000000l;
		
		//representing and storing decimal numbers
		float a=1.99f;
		double b=19.9876685758; //most popular data type to use for decimal numbers
		
		//representing and storing single character
		char character='#';
		char letter='c';
		
		//representing and storing boolean values (yes or no)
		boolean hungry=true; //yes
		boolean tired=false; //no
		
		System.out.println(box1);
		System.out.println("box1");
		//System.out.println(box); access only variables that you created
	}
}
